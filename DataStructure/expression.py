#Logical Operators
print('Logical Operators')
print(not True)
print(True and False)
print(True or False)
print('')

#Equality Operators
print('Equality Operators')
a=None
print(a is None)
print(a is not None)
print(a == None)
print(a!=None)

#Comparison Operators
print('Comparison Operators')
print(4>3)
print(3<5)
print('w'<='w')
print('q'>='w')

#Arithmetic Operators(+,-,*,/,//,%)
print('Arithmetic Operators')
q=11//5 #返回整数 2
print(q)
q=11%5 #返回余数1
print(q)
q=11/5 #返回float结果2.2
print(q)

#Bitwise Operators
print('Bitwise Operators')
print(~2)
print(1&3)
print(1|3)
print(2^2)
print(2<<2)








